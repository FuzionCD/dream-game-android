//     `RNG(0, 23, stream=4)` and returns it directly, picking any of the 24
//     variants uniformly, high-exit ones included. this is the typical case.
//
// (2) Ignorance-snag path. when snag kind 0x40 (Ignorance) is on the board,
//     FUN_1000203d4 routes through GameBoard::findGridIdxPool (port of
