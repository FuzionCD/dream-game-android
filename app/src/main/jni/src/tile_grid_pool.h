// 12 cosmetic layouts x 24 directionality variants per layout = 288 entries.
//
// each entry describes one of the 24 directionality variants for that layout:
//   exitCount = 2..5, the number of hex edges this variant has open
//   exits[]   = which of the 6 hex edges (0..5) are open, sorted ascending.
//               unused slots beyond exitCount are -1.
//
// distribution across all 288 entries (matches what shows up in gameplay):
//   exitCount=2 (path / curve):    119  (~41%)
